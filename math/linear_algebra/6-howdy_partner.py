#!/usr/bin/env python3

"""Concatenate two arrays."""


def cat_arrays(arr1, arr2):
    """
    @param arr1 - First array to concatenate.
    @param arr2 - Second array to concatenate.

    @return concatenated array of same type as arr1 and arr2 as a single array.
    """
    concat = arr1 + arr2
    return concat
